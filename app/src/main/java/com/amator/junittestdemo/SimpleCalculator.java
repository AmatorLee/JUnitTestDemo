public class SimpleCalculator {

    public int  abs(int num){
        return Math.abs(num);
    }

    public int sum(int a,int b){
        return a+b;
    }

    public int sub(int a,int b){
        return a-b;
    }

    public int mul(int a,int b){
        return a*b;
    }

    public int divide(int a,int b){
        return a/b;
    }

}
